// Отримуємо всі елементи списку
const listItems = document.querySelectorAll('#selectable-list li');

// Функція для вибору елемента
function selectItem(event) {
    // Видаляємо клас 'selected' у всіх елементів
    listItems.forEach(item => {
        item.classList.remove('selected');
    });

    // Додаємо клас 'selected' до поточного елемента
    event.target.classList.add('selected');
}

// Додаємо обробники подій для кожного елемента списку
listItems.forEach(item => {
    item.addEventListener('click', selectItem);
});
